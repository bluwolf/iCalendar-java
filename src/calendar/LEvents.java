package calendar;

public class LEvents {
	int year;
	int day;
	int Month;
	String Title;
	String Location;
	String startTime;
	String endTime;	
	public LEvents(int y, int d, int m, String t, String l, String s, String e) {
		year = y;
		day = d;
		Month = m;
		Title = t;
		Location = l;
		startTime = s;
		endTime = e;
	}

	

}
